/*Pré-incremento (++let)
✔️ Incrementa antes de usar o valor.
✔️ Retorna o valor já incrementado.

 Pós-incremento (let++)
✔️ Usa o valor primeiro, depois incrementa.
✔️ Retorna o valor antes da adição.

Quando usar cada um?
Use ++let (pré-incremento) se precisar do valor já incrementado imediatamente.
Use let++ (pós-incremento) se precisar do valor original antes de incrementar.

*/

let n=10;

n++  //pós incremento

console.log(++n) //pré incremento

console.log(n)

console.log(n++)

console.log(n)
